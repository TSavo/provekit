pub fn render_report(
    fixture_root: &Path,
    pass_1: &PassResult,
    pass_2: &PassResult,
    renamed_pair: Option<&(String, String)>,
) -> String {
    let mut s = String::new();
    let _ = writeln!(s, "# Smoke Test: End-to-End Vision Demo");
    let _ = writeln!(s);
    let _ = writeln!(
        s,
        "The eight-verb pipeline (Lift, Cluster, Name, Scope, Cluster, Identify, Realize, Witness) walking around a 200-line Rust fixture. Every contract in the rewritten output traces to one of three substrate sources: a lifted annotation, a lifted test assertion, or a wp_rule applied to a recognized term-shape. None of the contracts shown below were authored by the smoke-test driver author."
    );
    let _ = writeln!(s);
    let _ = writeln!(s, "Fixture root: `{}`", fixture_root.display());
    let _ = writeln!(s);

    section_1_fixture(&mut s);
    section_2_lift(&mut s, pass_1);
    section_3_clustering(&mut s, pass_1);
    section_4_bindings(&mut s, pass_1);
    section_5_propagation(&mut s, pass_1);
    section_6_compression(&mut s, pass_1);
    section_7_round_trip(&mut s, fixture_root);
    section_8_zero_authoring(&mut s, pass_1);
    section_9_open_karlton(&mut s, pass_1);
    section_10_naming_roundtrip(&mut s, pass_1, pass_2, renamed_pair);

    // Trailing receipt.
    let _ = writeln!(s);
    let _ = writeln!(s, "---");
    let _ = writeln!(
        s,
        "Generated by `cargo run -p smoke-test-e2e-driver` against the fixture at `menagerie/smoke-test-e2e/`."
    );
    let _ = writeln!(
        s,
        "All hashes are BLAKE3-512 self-identifying strings. All `mint_contract` envelopes are signed with the shared dev seed `[0x42; 32]` per the kit default; they are reproducible across machines."
    );

    s
}

