fn section_8_zero_authoring(s: &mut String, pass: &PassResult) {
    let _ = writeln!(s, "## 8. Zero-authoring receipt");
    let _ = writeln!(s);
    let _ = writeln!(
        s,
        "Every contract in the rewritten output, with its substrate source traced back to its origin:"
    );
    let _ = writeln!(s);
    let _ = writeln!(s, "| Site | Pre | Post | Origin |");
    let _ = writeln!(s, "|---|---|---|---|");
    for b in &pass.bindings {
        let pre = b.pretty_pre.clone().unwrap_or_else(|| "(none)".into());
        let post = b.pretty_post.clone().unwrap_or_else(|| "(none)".into());
        let _ = writeln!(
            s,
            "| `{}::{}` | `{}` | `{}` | {} |",
            b.site_file,
            b.site_fn,
            pre,
            post,
            origin_explanation(&b.contract_origin)
        );
    }
    let _ = writeln!(s);
    let _ = writeln!(s, "### Known transport losses");
    let _ = writeln!(s);
    let _ = writeln!(
        s,
        "- The driver's `formula_text_to_value` encoding wraps each pretty-printed contract string in a single-atom IR formula (`{{kind:atomic, name:\"<text>\", args:[]}}`). The mint envelope is signed correctly and the BLAKE3-512 CID is deterministic, but the formula is opaque to the kit's parse/serialize round-trip. Discharge verdict for these mints is `loudly-bounded-lossy(formula-string-transport)`. Closing this gap requires the upstream `provekit-ir-symbolic::parse` to accept the rewritten predicate strings; until then the smoke test's transport is signed-but-opaque."
    );
    let _ = writeln!(
        s,
        "- **[CLOSED — Stub 2]** The smoke-test driver now invokes the live `libprovekit::wp` evaluator for algebra-synthesis discharge. A `SmokeTestResolver` carries authored `wp_rule`s for `retry-loop` (`max_attempts >= 0 ∧ Q`) and `guard-then-commit` (`Q`). The evaluator reduces the rule against the postcondition placeholder and returns a ground formula; verdict is `exact`. The previously blocking condition (wp-as-formula PR series — PRs #619, #633, #663) is fully merged on main; this stub is now closed. Remaining loss on algebra-synthesis sites: the synthesized contract formula is still a single-atom shim (Stub 1 — `provekit-ir-symbolic::parse` round-trip not yet wired). The discharge ITSELF is exact; the formula encoding is the remaining open gap."
    );
    let _ = writeln!(
        s,
        "- **[CLOSED — Stub 3]** `ConceptSiteMemento` is now emitted with `schemaVersion: \"1\"` using the canonical `provekit_ir_types::ConceptSiteMemento` type (PR #692, commit 5919e46f). Each site with a recovered contract emits a fully-structured JSON artifact under `artifacts/`. Sites with no recovered contract are skip-emitted (see next bullet). `ConceptAbstractionMemento` remains on `stub-0` schema (its canonical spec is a separate landing)."
    );
    let _ = writeln!(
        s,
        "- **[Open — synthetic referents]** `provenance.{{clusterer_cid,discharger_cid,lifter_cid}}` and `discharge.discharge_receipt_cid` are synthetic deterministic hashes (`blake3-512` of a stable seed string). No real binary CIDs or `MorphismDischargeReceipt` objects exist yet (PR-B and PR-C are pending). The smoke test's CIDs are reproducible and content-addressed but do not reference objects in the pool. Closing this gap requires PR-B (lifter wiring) and PR-C (discharge wiring)."
    );
    let _ = writeln!(
        s,
        "- **[Open — skip-emitted sites]** The 3 sites with no recovered contract (`clamp_score`, `clamp_pressure`, `attempt_succeeds`) do not emit a `ConceptSiteMemento` because `local_contract_cid` is required by spec §1.1 and there is no contract to reference. These sites are recorded with `discharge_verdict: refuse(no contract recovered)` in the report but are absent from the artifacts pool. Closing this gap requires a `FunctionContractMemento` for empty-contract functions (e.g., a trivial `true` postcondition)."
    );
    let _ = writeln!(s);
}

fn section_9_open_karlton(s: &mut String, pass: &PassResult) {
    let _ = writeln!(s, "## 9. Open Karlton work");
    let _ = writeln!(s);
    let unnamed: Vec<&ConceptRecord> = pass
        .concepts
        .iter()
        .filter(|c| c.name.starts_with("UNNAMED-CONCEPT-"))
        .collect();
    if unnamed.is_empty() {
        let _ = writeln!(
            s,
            "Every cluster in pass 1 received a name from the seed catalog or a human annotation. No open naming work remains in this run."
        );
        let _ = writeln!(s);
        return;
    }
    let _ = writeln!(
        s,
        "{} cluster(s) surfaced without a name. The substrate places each one's stable shape-CID alongside a `// concept: UNNAMED-CONCEPT-N` annotation in the rewritten output, inviting a human to write the name where it surfaced. Section 10 demonstrates the closure path.",
        unnamed.len()
    );
    let _ = writeln!(s);
    let _ = writeln!(s, "| Unnamed | Shape-CID | First site |");
    let _ = writeln!(s, "|---|---|---|");
    for c in unnamed {
        let first = c
            .site_indices
            .first()
            .map(|idx| {
                let b = &pass.bindings[*idx];
                format!("`{}::{}`", b.site_file, b.site_fn)
            })
            .unwrap_or_else(|| "(no sites)".into());
        let _ = writeln!(s, "| `{}` | `{}` | {} |", c.name, c.shape_cid, first);
    }
    let _ = writeln!(s);
}

fn section_10_naming_roundtrip(
    s: &mut String,
    pass_1: &PassResult,
    pass_2: &PassResult,
    renamed_pair: Option<&(String, String)>,
) {
    let _ = writeln!(
        s,
        "## 10. The naming round-trip: how Karlton's second hard thing closes inline"
    );
    let _ = writeln!(s);
    let _ = writeln!(
        s,
        "Karlton: there are two hard things in computer science, cache invalidation and naming things. The substrate handles the first via content-addressed mementos. This section handles the second."
    );
    let _ = writeln!(s);
    let _ = writeln!(
        s,
        "When the substrate emits rewritten code with `// concept: UNNAMED-CONCEPT-N` above a cluster it could not name, that annotation IS substrate input on the next lift. A human reads the rewritten code in their editor, replaces `UNNAMED-CONCEPT-N` with a real name, and the next pass picks it up. The concept's shape-CID is stable; only the name changes."
    );
    let _ = writeln!(s);
    let _ = writeln!(s, "### Annotation format");
    let _ = writeln!(s);
    let _ = writeln!(s, "```rust");
    let _ = writeln!(s, "// concept: <name>");
    let _ = writeln!(s, "[other substrate-emitted attributes...]");
    let _ = writeln!(s, "pub fn the_function(...) {{ ... }}");
    let _ = writeln!(s, "```");
    let _ = writeln!(s);
    let _ = writeln!(
        s,
        "The `<name>` is a bare identifier (no `concept:` prefix). The lifter scans upward from each `fn` declaration across attribute lines for the first `// concept: <name>` and uses it. Whitespace is trimmed. Anything that is not a `#[...]` line breaks the upward scan."
    );
    let _ = writeln!(s);

    let _ = writeln!(s, "### Pass 1 -> Pass 1.5 -> Pass 2");
    let _ = writeln!(s);
    if let Some((shape_cid, new_name)) = renamed_pair {
        let _ = writeln!(
            s,
            "Pass 1 surfaced an unnamed cluster at shape-CID `{}`. The driver wrote `// concept: UNNAMED-CONCEPT-N` above one of the cluster's sites in `rewritten/src/...`.",
            short_cid(shape_cid)
        );
        let _ = writeln!(s);
        let _ = writeln!(
            s,
            "Pass 1.5 (simulated human edit, executed by `naming_roundtrip::apply_human_naming`) replaces that annotation with `// concept: {}`. The change is exactly what a human editor would commit. The substrate did not generate the name; the substrate ASKED for it, the human SUPPLIED it, the substrate now LEARNS it.",
            new_name
        );
        let _ = writeln!(s);
        let _ = writeln!(
            s,
            "Pass 2 re-lifts the same source tree (now with the human-edited annotation) and binds the same shape-CID to `concept:{}`. Every binding to that shape-CID inherits the new name without any further human action.",
            new_name
        );
        let _ = writeln!(s);
        let _ = writeln!(s, "Before pass 2:");
        let _ = writeln!(s);
        let _ = writeln!(
            s,
            "- concept at shape-CID `{}` had name `UNNAMED-CONCEPT-N`",
            short_cid(shape_cid)
        );
        let pre_sites = sites_for_shape(pass_1, shape_cid);
        for (file, fn_name) in &pre_sites {
            let _ = writeln!(
                s,
                "  - binding at `{}::{}` reported under `UNNAMED-CONCEPT-N`",
                file, fn_name
            );
        }
        let _ = writeln!(s);
        let _ = writeln!(s, "After pass 2:");
        let _ = writeln!(s);
        // The pass-2 source root is `rewritten/`, so file paths look like
        // `src/<basename>.rs` relative to rewritten.
        let post_sites = sites_for_shape(pass_2, shape_cid);
        let post_name = pass_2
            .concepts
            .iter()
            .find(|c| c.shape_cid == *shape_cid)
            .map(|c| c.name.clone())
            .unwrap_or_else(|| format!("concept:{}", new_name));
        let _ = writeln!(
            s,
            "- concept at shape-CID `{}` has name `{}`",
            short_cid(shape_cid),
            post_name
        );
        for (file, fn_name) in &post_sites {
            let _ = writeln!(
                s,
                "  - binding at `{}::{}` reported under `{}`",
                file, fn_name, post_name
            );
        }
        let _ = writeln!(s);
        let _ = writeln!(
            s,
            "The shape-CID did not move. The name attached. Every other binding inherited the new name through the concept-CID, not through a code change."
        );
    } else {
        let _ = writeln!(
            s,
            "Pass 1 did not surface any UNNAMED concept; the seed catalog covered every cluster. The naming round-trip mechanism is unexercised in this run but is implemented and documented above. The next fixture that uses a novel shape will exercise it."
        );
    }
    let _ = writeln!(s);
    let _ = writeln!(
        s,
        "Karlton's second hard thing closes inline. No catalog editor, no separate naming workflow. The unnamed thing surfaces where the code is. The name lands where the unnamed thing surfaced. The substrate learns on the next pass."
    );
    let _ = writeln!(s);
}

// ----- helpers --------------------------------------------------------------

fn sites_for_shape(pass: &PassResult, shape_cid: &str) -> Vec<(String, String)> {
    // Resolve to a concept (looking through aliases) and return every
    // binding that resolved to that concept regardless of which alias
    // shape produced it.
    let concept_idx_opt = pass.concepts.iter().position(|c| {
        c.shape_cid == shape_cid || c.shape_cid_aliases.iter().any(|a| a == shape_cid)
    });
    let Some(ci) = concept_idx_opt else {
        return Vec::new();
    };
    let mut out = Vec::new();
    for idx in &pass.concepts[ci].site_indices {
        let b = &pass.bindings[*idx];
        out.push((b.site_file.clone(), b.site_fn.clone()));
    }
    out
}

fn count_unique_files(pass: &PassResult) -> usize {
    let mut set: BTreeMap<String, ()> = BTreeMap::new();
    for b in &pass.bindings {
        set.insert(b.site_file.clone(), ());
    }
    set.len()
}

fn describe_name_source(n: &NameSource) -> &'static str {
    match n {
        NameSource::Auto => "auto (UNNAMED-CONCEPT-N)",
        NameSource::Catalog => "seed catalog",
        NameSource::HumanAnnotation => "human annotation",
    }
}

fn origin_explanation(o: &ContractOrigin) -> String {
    match o {
        ContractOrigin::AttributeLift => {
            "annotation-lift (lifted from `#[requires]`/`#[ensures]` on the source)".into()
        }
        ContractOrigin::TestLift => {
            "test-lift (lifted from `assert!` in `tests/properties.rs`)".into()
        }
        ContractOrigin::AlgebraSynthesis { rule_id } => format!(
            "algebra-synthesis (wp_rule `{}` fired structurally on the cluster)",
            rule_id
        ),
        ContractOrigin::Empty => "(no contract recovered)".into(),
    }
}

fn short_cid(s: &str) -> String {
    // `blake3-512:` + 16 hex chars.
    let prefix = "blake3-512:";
    if let Some(rest) = s.strip_prefix(prefix) {
        let truncated: String = rest.chars().take(16).collect();
        format!("{}{}\u{2026}", prefix, truncated)
    } else if s.len() > 24 {
        format!("{}\u{2026}", &s[..24])
    } else {
        s.to_string()
    }
}
