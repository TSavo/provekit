fn section_3_clustering(s: &mut String, pass: &PassResult) {
    let _ = writeln!(s, "## 3. The clustering");
    let _ = writeln!(s);
    let _ = writeln!(
        s,
        "Shape-CIDs were grouped via exact equality (the algebra's address). Each cluster received a name via the priority order: human annotation (pass 2 only) -> seed catalog -> `UNNAMED-CONCEPT-N`."
    );
    let _ = writeln!(s);
    let _ = writeln!(
        s,
        "| Concept | Shape-CID (prefix) | Source of Name | # sites | Catalog id |"
    );
    let _ = writeln!(s, "|---|---|---|---|---|");
    for c in &pass.concepts {
        let _ = writeln!(
            s,
            "| `{}` | `{}` | {} | {} | {} |",
            c.name,
            short_cid(&c.shape_cid),
            describe_name_source(&c.name_source),
            c.site_indices.len(),
            c.catalog_id.clone().unwrap_or_else(|| "(none)".into()),
        );
    }

    let unnamed: Vec<&ConceptRecord> = pass
        .concepts
        .iter()
        .filter(|c| matches!(c.name_source, NameSource::Auto))
        .collect();
    if !unnamed.is_empty() {
        let _ = writeln!(s);
        let _ = writeln!(
            s,
            "{} unnamed cluster(s) surfaced in pass 1. Each carries a stable shape-CID. The naming round-trip in section 10 closes one of them.",
            unnamed.len()
        );
    }
    let _ = writeln!(s);
}

fn section_4_bindings(s: &mut String, pass: &PassResult) {
    let _ = writeln!(s, "## 4. The bindings");
    let _ = writeln!(s);
    let _ = writeln!(
        s,
        "Each binding is a `(code-site -> concept -> discharge verdict)` triple. The discharge verdict is computed by a structural oracle that respects the substrate's loss model:"
    );
    let _ = writeln!(s);
    let _ = writeln!(
        s,
        "- `exact` if both shape and formula are losslessly transported."
    );
    let _ = writeln!(
        s,
        "- `loudly-bounded-lossy(...)` when the shape transports exactly but the formula uses the smoke-test's single-atom encoding (see section 8)."
    );
    let _ = writeln!(s, "- `refuse(...)` if no contract was recovered.");
    let _ = writeln!(s);
    let _ = writeln!(s, "| Site | Concept | Origin | Discharge Verdict |");
    let _ = writeln!(s, "|---|---|---|---|");
    for b in &pass.bindings {
        let concept_name = pass.concepts[b.concept_idx].name.clone();
        let _ = writeln!(
            s,
            "| `{}::{}` | `{}` | `{}` | `{}` |",
            b.site_file,
            b.site_fn,
            concept_name,
            b.contract_origin.label(),
            b.discharge_verdict.label(),
        );
    }
    let _ = writeln!(s);
}

fn section_5_propagation(s: &mut String, pass: &PassResult) {
    let _ = writeln!(s, "## 5. The propagation event");
    let _ = writeln!(s);
    if pass.witnesses.is_empty() {
        let _ = writeln!(s, "No witnesses lifted in pass 1.");
        let _ = writeln!(s);
        return;
    }
    let _ = writeln!(
        s,
        "One unit-test assertion lifted into the substrate as a `WitnessMemento` and attached to a concept (not a single site). Every other binding to the same concept-CID inherits the witness obligation. Before-and-after shown explicitly below."
    );
    for w in &pass.witnesses {
        render_witness_event(s, pass, w);
    }
    let _ = writeln!(s);
}

fn render_witness_event(s: &mut String, pass: &PassResult, w: &WitnessRecord) {
    let _ = writeln!(s);
    let _ = writeln!(s, "### Witness event");
    let _ = writeln!(s);
    let _ = writeln!(s, "- Source: `{}`", w.source_location);
    let _ = writeln!(
        s,
        "- Concept shape-CID: `{}`",
        short_cid(&w.concept_shape_cid)
    );
    let _ = writeln!(s, "- Formula (substrate-pretty): `{}`", w.pretty_formula);
    let _ = writeln!(s);
    let _ = writeln!(
        s,
        "**Before the witness fires**: the sites bound to this concept have no inherited obligation. The driver's per-site contract origins are recorded individually."
    );
    let _ = writeln!(s);
    let _ = writeln!(s, "| Site | Pre-witness Origin | Pre-witness Verdict |");
    let _ = writeln!(s, "|---|---|---|");
    for idx in &w.propagated_to {
        let b = &pass.bindings[*idx];
        let _ = writeln!(
            s,
            "| `{}::{}` | `{}` | `{}` |",
            b.site_file,
            b.site_fn,
            b.contract_origin.label(),
            b.discharge_verdict.label(),
        );
    }
    let _ = writeln!(s);
    let _ = writeln!(
        s,
        "**After the witness fires**: the concept-CID carries the obligation. Every binding to that shape-CID inherits a `#[cfg_attr(any(), witness(...))]` marker in the rewritten output. The propagated-to set is exactly the concept's site list."
    );
    let _ = writeln!(s);
    let _ = writeln!(s, "| Site | Inherited Witness |");
    let _ = writeln!(s, "|---|---|");
    for idx in &w.propagated_to {
        let b = &pass.bindings[*idx];
        let _ = writeln!(
            s,
            "| `{}::{}` | `{}` |",
            b.site_file, b.site_fn, w.pretty_formula
        );
    }
    let _ = writeln!(s);
    let _ = writeln!(
        s,
        "The propagation is the magic: one assertion against one function attaches a property to N sites by going through the concept abstraction. The witness travels with the algebra, not with the code-site."
    );
}

fn section_6_compression(s: &mut String, pass: &PassResult) {
    let _ = writeln!(s, "## 6. The compression event");
    let _ = writeln!(s);

    // Find concepts with > 1 site.
    let multi: Vec<&ConceptRecord> = pass
        .concepts
        .iter()
        .filter(|c| c.site_indices.len() >= 2)
        .collect();
    if multi.is_empty() {
        let _ = writeln!(s, "No multi-site concept-clusters found in pass 1.");
        let _ = writeln!(s);
        return;
    }
    let _ = writeln!(
        s,
        "When the algebra's address is the cluster key, syntactic variants of the same idea fall into one bucket. The fixture has two such clusters; both compress automatically."
    );
    for c in multi {
        let _ = writeln!(s);
        let _ = writeln!(s, "### Cluster: `{}`", c.name);
        let _ = writeln!(s);
        let _ = writeln!(s, "- Primary shape-CID: `{}`", c.shape_cid);
        if !c.shape_cid_aliases.is_empty() {
            let _ = writeln!(s, "- Alias shape-CIDs (compressed into the same cluster):");
            for alias in &c.shape_cid_aliases {
                let _ = writeln!(s, "  - `{}`", alias);
            }
        }
        let _ = writeln!(s, "- Sites:");
        for idx in &c.site_indices {
            let b = &pass.bindings[*idx];
            let alias_note = if b.shape_cid == c.shape_cid {
                String::from("(primary shape)")
            } else {
                format!("(alias shape `{}`)", short_cid(&b.shape_cid))
            };
            let _ = writeln!(
                s,
                "  - `{}::{}` (line {}) realizes `{}` {}",
                b.site_file, b.site_fn, b.site_line, c.name, alias_note
            );
        }
        let _ = writeln!(s);
        if c.shape_cid_aliases.is_empty() {
            let _ = writeln!(
                s,
                "Every site in this cluster has the same canonical shape-CID. The compression is byte-identical at the algebra level."
            );
        } else {
            let _ = writeln!(
                s,
                "The driver did not need to be told these functions are related. They were written with different syntax (different loop forms, different mutation patterns) and the canonical term-shape classifier collapses them into one cluster across `{}` distinct shape-CIDs. That is the algebra compression event for this cluster: structurally different term-shapes recognized by the catalog's classification rule and merged under one named concept.",
                1 + c.shape_cid_aliases.len()
            );
        }
    }
    let _ = writeln!(s);
}

