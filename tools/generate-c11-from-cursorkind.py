def parse_cursor_enum(index_h: Path) -> list[dict]:
    text = index_h.read_text(encoding="utf-8")
    m = re.search(r"enum\s+CXCursorKind\s*\{(?P<body>.*?)\n\};", text, re.S)
    if not m:
        raise SystemExit(f"enum CXCursorKind not found in {index_h}")
    body = re.sub(r"/\*.*?\*/", "", m.group("body"), flags=re.S)
    body = re.sub(r"//.*", "", body)
    values: dict[str, int] = {}
    entries: list[dict] = []
    next_value = 0
    for raw in body.splitlines():
        line = raw.strip()
        if not line or not line.startswith("CXCursor_"):
            continue
        mm = re.match(r"(CXCursor_[A-Za-z0-9_]+)\s*(?:=\s*([^,]+))?\s*,?", line)
        if not mm:
            continue
        name, expr = mm.group(1), mm.group(2)
        alias_of = None
        if expr is None:
            value = next_value
        else:
            expr = expr.strip()
            if re.fullmatch(r"0x[0-9A-Fa-f]+|\d+", expr):
                value = int(expr, 0)
            elif expr in values:
                value = values[expr]
                alias_of = expr
            else:
                continue
        values[name] = value
        next_value = value + 1
        if name.startswith("CXCursor_First") or name.startswith("CXCursor_Last"):
            continue
        entries.append({"cursor_kind": name, "value": value, "alias_of": alias_of})
    return entries


def op_name_for_spec(spec: str) -> str:
    path = SPECS / spec
    if path.exists():
        try:
            value = json.loads(path.read_text(encoding="utf-8"))
            fn_name = value.get("fn_name", "")
            if fn_name.startswith("c11:"):
                return fn_name.removeprefix("c11:")
        except (OSError, json.JSONDecodeError):
            pass
    return spec.removeprefix("op_").removesuffix(".spec.json").replace("_", "-")


def cids_by_op() -> tuple[dict[str, str], str | None, list[dict]]:
    entries = []
    cids_path = SIG / "cids.tsv"
    if cids_path.exists():
        for line in cids_path.read_text(encoding="utf-8").splitlines():
            parts = line.split("\t")
            if len(parts) >= 3:
                entry = {
                    "kind": parts[0],
                    "spec": parts[1],
                    "cid": parts[2],
                }
                if len(parts) >= 4:
                    entry["path"] = parts[3]
                entries.append(entry)
    elif (SIG / "component-cids.json").exists():
        entries = json.loads((SIG / "component-cids.json").read_text(encoding="utf-8"))

    op_cids: dict[str, str] = {}
    signature_cid = None
    for entry in entries:
        spec = entry.get("spec", "")
        cid = entry.get("cid")
        if not cid:
            continue
        if spec.startswith("op_") and spec.endswith(".spec.json"):
            op_cids[op_name_for_spec(spec)] = cid
        elif spec == "language_signature_c11.spec.json":
            signature_cid = cid
        elif spec.startswith("eff_op_") and spec.endswith(".spec.json"):
            name = "effect:" + spec.removeprefix("eff_op_").removesuffix(".spec.json")
            op_cids[name] = cid
    return op_cids, signature_cid, entries


