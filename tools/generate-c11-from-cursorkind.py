def generated_c_source(mapping: dict, op_cids: dict[str, str]) -> str:
    rows = [r for r in mapping["rows"] if not r.get("alias_of")]
    signature_cid = mapping.get("signature_cid") or ""
    lines = [
        "/* SPDX-License-Identifier: Apache-2.0 */",
        f"/* {GENERATED} */",
        '#include "c11_cursor_dispatch.generated.h"',
        "",
        "#ifdef PK_C_ENABLE_CLANG_AST",
        "#include <string.h>",
        "",
        "const char *pk_c11_signature_cid(void) {",
        f"    return {c_string(signature_cid)};",
        "}",
        "",
        "const pk_c11_cursor_dispatch *pk_c11_cursor_dispatch_lookup(enum CXCursorKind kind) {",
        "    switch (kind) {",
    ]
    for row in rows:
        lines.extend(
            [
                f"    case {row['value']}: /* {row['cursor_kind']} */ {{",
                "        static const pk_c11_cursor_dispatch info = {",
                f"            (enum CXCursorKind){row['value']}, /* {row['cursor_kind']} */",
                f"            {c_string(row['cursor_kind'])},",
                f"            {c_string(row.get('op_name'))},",
                f"            {c_string(row.get('op_cid'))},",
                f"            {int(row.get('arity') or 0)},",
                f"            {c_string(json.dumps(row.get('arity_shape'), sort_keys=True) if row.get('arity_shape') is not None else None)},",
                f"            {c_string(row.get('child_shape'))},",
                f"            {c_string(row.get('classification'))},",
                f"            {c_string(row.get('notes'))}",
                "        };",
                "        return &info;",
                "    }",
            ]
        )
    lines.extend(
        [
            "    }",
            "    return NULL;",
            "}",
            "",
            "static const pk_c11_op_dispatch PK_C11_OPS[] = {",
        ]
    )
    all_ops = OrderedDict((name, op_cids.get(name)) for name in all_operation_names())
    missing_templates = sorted(set(all_ops) - set(SERIALIZER_TEMPLATES))
    extra_templates = sorted(set(SERIALIZER_TEMPLATES) - set(all_ops))
    if missing_templates or extra_templates:
        raise SystemExit(
            "serializer template mismatch: "
            f"missing={','.join(missing_templates)} extra={','.join(extra_templates)}"
        )
    serializable = set(SERIALIZER_TEMPLATES)
    for name, cid in all_ops.items():
        lines.append(f"    {{{c_string('c11:' + name)}, {c_string(cid)}, {1 if name in serializable else 0}}},")
    lines.extend(
        [
            "    {NULL, NULL, 0}",
            "};",
            "",
            "const pk_c11_op_dispatch *pk_c11_op_dispatch_lookup(const char *op_name) {",
            "    if (op_name == NULL) return NULL;",
            "    for (size_t i = 0; PK_C11_OPS[i].op_name != NULL; i++) {",
            "        if (strcmp(PK_C11_OPS[i].op_name, op_name) == 0) return &PK_C11_OPS[i];",
            "    }",
            "    return NULL;",
            "}",
            "",
            "#endif",
            "",
        ]
    )
    return "\n".join(lines)


def refresh_component_cids(entries: list[dict]) -> dict[Path, str]:
    if not entries:
        return {}
    return {SIG / "component-cids.json": json.dumps(entries, indent=2, sort_keys=False) + "\n"}


def generated_readme(signature_cid: str | None) -> str:
    path = SIG / "README.md"
    old = path.read_text(encoding="utf-8") if path.exists() else "# C11 Language Signature Memento\n"
    cid = signature_cid or "PENDING"
    old = re.sub(
        r"The minted C11 `LanguageSignatureMemento` is:\n\n`[^`]+`",
        f"The minted C11 `LanguageSignatureMemento` is:\n\n`{cid}`",
        old,
    )
    rows = [
        "| Operation | Arity | Result | Contract meaning |",
        "| --- | --- | --- | --- |",
    ]
    for spec in all_operation_specs():
        spec_path = SPECS / spec
        if spec_path.exists():
            value = json.loads(spec_path.read_text(encoding="utf-8"))
        elif spec.startswith("op_") and spec.endswith(".spec.json"):
            matches = [
                name
                for name in list(MINTED_OPS) + list(OPERATOR_OPS)
                if spec_filename_for_op(name) == spec
            ]
            op_name = matches[0] if matches else spec.removeprefix("op_").removesuffix(".spec.json").replace("_", "-")
            value = algorithm_spec(op_name, (MINTED_OPS.get(op_name) or OPERATOR_OPS[op_name]))
        else:
            continue
        name = value["fn_name"].removeprefix("c11:")
        post = value.get("post", {})
        arity = " x ".join(post.get("arity", [])) or "Unit"
        result = post.get("result") or value.get("return_sort", {}).get("name", "")
        wp = post.get("wp", "")
        rows.append(f"| `{name}` | `{arity}` | `{result}` | {wp} |")
    table = "\n".join(rows)
    if "## Generated Cursor Kind Operations" not in old:
        old = old.replace("## Equations", "## Generated Cursor Kind Operations\n\nAdditional operations below are generated from `enum CXCursorKind` for cursor kinds that do not fit the hand-curated core. Non-lifted declarations, attributes, preprocessing cursors, and non-C-family extensions map to `opaque`.\n\n## Equations")
    old = re.sub(r"## Operations\n\n.*?\n\n## Generated Cursor Kind Operations", f"## Operations\n\n{table}\n\n## Generated Cursor Kind Operations", old, flags=re.S)
    return old


def generated_files(index_h: Path) -> dict[Path, str]:
    op_cids, signature_cid, cid_entries = cids_by_op()
    enum_entries = parse_cursor_enum(index_h)
    mapping = build_mapping(enum_entries, op_cids, signature_cid, index_h)
    files: dict[Path, str] = {}
    files.update(generated_op_specs())
    files[SPECS / "language_signature_c11.spec.json"] = language_signature_spec()
    files[SIG / "mint.sh"] = mint_sh()
    files[MAPPING] = json.dumps(mapping, indent=2, sort_keys=False) + "\n"
    files[C_HDR] = generated_c_header()
    files[C_SRC] = generated_c_source(mapping, op_cids)
    files[SIG / "README.md"] = generated_readme(signature_cid)
    files.update(refresh_component_cids(cid_entries))
    return files


def write_files(files: dict[Path, str]) -> None:
    for path, content in files.items():
        path.parent.mkdir(parents=True, exist_ok=True)
        path.write_text(content, encoding="utf-8")
        if path.name == "mint.sh" or path.name == "generate-c11-from-cursorkind.py":
            path.chmod(0o755)


def check_files(files: dict[Path, str]) -> int:
    stale = []
    for path, expected in files.items():
        try:
            actual = path.read_text(encoding="utf-8")
        except FileNotFoundError:
            stale.append((path, "missing"))
            continue
        if actual != expected:
            stale.append((path, "stale"))
    if stale:
        for path, reason in stale:
            print(f"{path}: {reason}", file=sys.stderr)
        return 1
    return 0


def print_summary(files: dict[Path, str]) -> None:
    mapping = json.loads(files[MAPPING])
    rows = mapping["rows"]
    counts: dict[str, int] = {}
    for row in rows:
        counts[row["classification"]] = counts.get(row["classification"], 0) + 1
    print(f"source: {mapping['source']}")
    print(f"cursor kinds: {mapping['cursor_kind_count']}")
    for key in sorted(counts):
        print(f"{key}: {counts[key]}")
    print(f"signature_cid: {mapping.get('signature_cid') or 'PENDING'}")


def main(argv: list[str]) -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument(
        "--index-h",
        help="Path to a CXCursorKind snapshot or clang-c/Index.h for deliberate snapshot updates",
    )
    parser.add_argument("--check", action="store_true", help="Fail if generated files are stale")
    parser.add_argument("--summary", action="store_true", help="Print mapping summary")
    args = parser.parse_args(argv)

    index_h = find_cursor_snapshot(args.index_h)
    files = generated_files(index_h)
    if args.check:
        return check_files(files)
    write_files(files)
    if args.summary:
        print_summary(files)
    return 0


if __name__ == "__main__":
    raise SystemExit(main(sys.argv[1:]))
