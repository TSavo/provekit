def mint_sh() -> str:
    def join(items: list[str]) -> str:
        return " ".join(items)

    return (
        "#!/bin/sh\n"
        "set -eu\n"
        "BASE=\"$(CDPATH= cd -- \"$(dirname -- \"$0\")\" && pwd)\"\n"
        "ROOT=\"$(CDPATH= cd -- \"$BASE/../..\" && pwd)\"\n"
        "RUST_DIR=\"$ROOT/implementations/rust\"\n"
        "PROVEKIT=\"$RUST_DIR/target/debug/provekit\"\n"
        "SPEC_DIR=\"$BASE/specs\"\n"
        "CATALOG_REAL=\"$BASE/catalog\"\n"
        "CATALOG_ARG=\"$BASE/dev/../catalog\"\n"
        "CID_FILE=\"$BASE/cids.tsv\"\n"
        "mkdir -p \"$BASE/dev\"\n"
        "rm -rf \"$CATALOG_REAL\"\n"
        "cargo build --manifest-path \"$RUST_DIR/Cargo.toml\" -p provekit-cli -p provekit-ir-compiler-maude\n"
        ": > \"$CID_FILE\"\n"
        "mint_one() {\n"
        "  kind=\"$1\"\n"
        "  spec=\"$2\"\n"
        "  out=$(\"$PROVEKIT\" mint \"$kind\" --spec \"$SPEC_DIR/$spec\" --unsigned --catalog \"$CATALOG_ARG\")\n"
        "  printf '%s\\t%s\\t%s\\n' \"$kind\" \"$spec\" \"$out\" | tee -a \"$CID_FILE\"\n"
        "}\n"
        f"for spec in {join(SORTS)}; do\n"
        "  mint_one sort \"$spec\"\n"
        "done\n"
        f"for spec in {join(op_spec_files())}; do\n"
        "  mint_one algorithm \"$spec\"\n"
        "done\n"
        f"for spec in {join(EQUATIONS)}; do\n"
        "  mint_one equation \"$spec\"\n"
        "done\n"
        f"for spec in {join(EFFECT_SIGS)}; do\n"
        "  mint_one effect-signature \"$spec\"\n"
        "done\n"
        "mint_one language-signature language_signature_c11.spec.json\n"
    )


def is_opaque_kind(name: str) -> bool:
    return name in OPAQUE_EXACT or name.startswith(OPAQUE_PREFIXES) or name.endswith("Attr")


def classify_entry(entry: dict, op_cids: dict[str, str]) -> dict:
    name = entry["cursor_kind"]
    row = {
        "cursor_kind": name,
        "value": entry["value"],
        "alias_of": entry.get("alias_of"),
        "op_name": "c11:opaque",
        "op_cid": op_cids.get("opaque"),
        "arity": 1,
        "arity_shape": arity_shape_for_operation("opaque"),
        "child_shape": "unit",
        "classification": "opaque",
        "notes": "Non-lifted cursor kind maps stably to c11:opaque.",
    }
    if entry.get("alias_of"):
        row["notes"] = f"Alias of {entry['alias_of']}; uses the target cursor-kind mapping."
    if name in CORE_CURSOR_MAP:
        op, arity, shape = CORE_CURSOR_MAP[name]
        row.update(
            {
                "op_name": f"c11:{op}",
                "op_cid": op_cids.get(op),
                "arity": arity,
                "arity_shape": arity_shape_for_operation(op),
                "child_shape": shape,
                "classification": "existing-core",
                "notes": "Reuses an existing hand-curated C11 core op.",
            }
        )
    elif name in LEAF_CURSOR_MAP:
        op, arity, shape = LEAF_CURSOR_MAP[name]
        row.update(
            {
                "op_name": op,
                "op_cid": None,
                "arity": arity,
                "arity_shape": None,
                "child_shape": shape,
                "classification": "term-leaf",
                "notes": "Leaf term constructor, not a minted operation.",
            }
        )
    elif name in DISPATCH_CURSOR_MAP:
        cfg = DISPATCH_CURSOR_MAP[name]
        dispatch = {
            "tokens": [
                {
                    "token": token,
                    "op_name": f"c11:{op}",
                    "op_cid": op_cids.get(op),
                    "arity_shape": arity_shape_for_operation(op),
                }
                for token, op in cfg["token_ops"].items()
            ],
            "fallback": {
                "op_name": f"c11:{cfg['fallback']}",
                "op_cid": op_cids.get(cfg["fallback"]),
                "arity_shape": arity_shape_for_operation(cfg["fallback"]),
            },
        }
        row.update(
            {
                "op_name": cfg["op_name"],
                "op_cid": None,
                "arity": cfg["arity"],
                "arity_shape": None,
                "child_shape": cfg["child_shape"],
                "classification": "operator-dispatch",
                "notes": "Operator spelling selects one of the listed op CIDs.",
                "operator_dispatch": dispatch,
            }
        )
    elif name in MINTED_CURSOR_MAP:
        op, arity, shape = MINTED_CURSOR_MAP[name]
        row.update(
            {
                "op_name": f"c11:{op}",
                "op_cid": op_cids.get(op),
                "arity": arity,
                "arity_shape": arity_shape_for_operation(op),
                "child_shape": shape,
                "classification": "newly-minted",
                "notes": "Generated minimal stable op for this libclang cursor kind.",
            }
        )
    elif name in TRANSPARENT_CURSOR_KINDS:
        row.update(
            {
                "op_name": "passthrough",
                "op_cid": None,
                "arity": 1,
                "arity_shape": None,
                "child_shape": TRANSPARENT_CURSOR_KINDS[name],
                "classification": "passthrough",
                "notes": "Parser unwraps this cursor kind and emits its child term.",
            }
        )
    elif not is_opaque_kind(name):
        if 100 <= entry["value"] <= 199:
            row.update(
                {
                    "op_name": "c11:unexposed-expr",
                    "op_cid": op_cids.get("unexposed-expr"),
                    "arity": 1,
                    "arity_shape": arity_shape_for_operation("unexposed-expr"),
                    "child_shape": "folded child expressions",
                    "classification": "newly-minted",
                    "notes": "Generated fallback expression op for a C-family expression cursor.",
                }
            )
        elif 200 <= entry["value"] <= 399:
            row.update(
                {
                    "op_name": "c11:unexposed-stmt",
                    "op_cid": op_cids.get("unexposed-stmt"),
                    "arity": 1,
                    "arity_shape": arity_shape_for_operation("unexposed-stmt"),
                    "child_shape": "folded child statements",
                    "classification": "newly-minted",
                    "notes": "Generated fallback statement op for a C-family statement cursor.",
                }
            )
    return row


def build_mapping(entries: list[dict], op_cids: dict[str, str], signature_cid: str | None, index_h: Path) -> dict:
    rows = [classify_entry(e, op_cids) for e in entries]
    return {
        "generated_by": GENERATOR,
        "source": source_path_for(index_h),
        "signature_cid": signature_cid,
        "serializers": [
            {
                "op_name": f"c11:{name}",
                "op_cid": op_cids.get(name),
                "render_template": template,
            }
            for name, template in SERIALIZER_TEMPLATES.items()
        ],
        "cursor_kind_count": len(rows),
        "rows": rows,
    }


def c_string(value: str | None) -> str:
    if value is None:
        return "NULL"
    return json.dumps(value)


